package lcmt.domain;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
@Entity(name="mst_sfco_type")
public class StandardFormContractType {
	@Id
	@GeneratedValue
	private int sfco_type_id;
	private String sfco_type_name;
	private Date sfco_type_created_at;
	private int sfco_type_added_by;
	private Date sfco_type_updated_at;
	
	public int getSfco_type_id() {
		return sfco_type_id;
	}
	public String getSfco_type_name() {
		return sfco_type_name;
	}
	public Date getSfco_type_created_at() {
		return sfco_type_created_at;
	}
	public int getSfco_type_added_by() {
		return sfco_type_added_by;
	}
	public Date getSfco_type_updated_at() {
		return sfco_type_updated_at;
	}
	public void setSfco_type_id(int sfco_type_id) {
		this.sfco_type_id = sfco_type_id;
	}
	public void setSfco_type_name(String sfco_type_name) {
		this.sfco_type_name = sfco_type_name;
	}
	public void setSfco_type_created_at(Date sfco_type_created_at) {
		this.sfco_type_created_at = sfco_type_created_at;
	}
	public void setSfco_type_added_by(int sfco_type_added_by) {
		this.sfco_type_added_by = sfco_type_added_by;
	}
	public void setSfco_type_updated_at(Date sfco_type_updated_at) {
		this.sfco_type_updated_at = sfco_type_updated_at;
	}
	
}
