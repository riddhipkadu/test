package lcmt.domain;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;

@Entity(name="trn_executed_contract_documents")
public class ExecutedContractDocuments {

	@Id
	@GeneratedValue
	private int exec_doc_id;
	private String exec_original_file_name;
	private String exec_generated_file_name;
	private int exec_contract_id;
	private int exec_last_generated_value_for_contract_id;
	private Date exec_contract_created_date;
	private int exec_doc_added_by;
	private int exec_doc_type; //1:exec doc and 2: amendment doc 3: term/renwal document
	
	
	
	
	
	public int getExec_doc_type() {
		return exec_doc_type;
	}
	public void setExec_doc_type(int exec_doc_type) {
		this.exec_doc_type = exec_doc_type;
	}
	public int getExec_doc_id() {
		return exec_doc_id;
	}
	public String getExec_original_file_name() {
		return exec_original_file_name;
	}
	public String getExec_generated_file_name() {
		return exec_generated_file_name;
	}
	public int getExec_contract_id() {
		return exec_contract_id;
	}
	public int getExec_last_generated_value_for_contract_id() {
		return exec_last_generated_value_for_contract_id;
	}
	public Date getExec_contract_created_date() {
		return exec_contract_created_date;
	}
	public void setExec_doc_id(int exec_doc_id) {
		this.exec_doc_id = exec_doc_id;
	}
	public void setExec_original_file_name(String exec_original_file_name) {
		this.exec_original_file_name = exec_original_file_name;
	}
	public void setExec_generated_file_name(String exec_generated_file_name) {
		this.exec_generated_file_name = exec_generated_file_name;
	}
	public void setExec_contract_id(int exec_contract_id) {
		this.exec_contract_id = exec_contract_id;
	}
	public void setExec_last_generated_value_for_contract_id(int exec_last_generated_value_for_contract_id) {
		this.exec_last_generated_value_for_contract_id = exec_last_generated_value_for_contract_id;
	}
	public void setExec_contract_created_date(Date exec_contract_created_date) {
		this.exec_contract_created_date = exec_contract_created_date;
	}
	public int getExec_doc_added_by() {
		return exec_doc_added_by;
	}
	public void setExec_doc_added_by(int exec_doc_added_by) {
		this.exec_doc_added_by = exec_doc_added_by;
	}
	
	
	
}
