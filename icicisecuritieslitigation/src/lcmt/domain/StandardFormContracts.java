package lcmt.domain;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.validation.constraints.Size;

@Entity(name="mst_standard_form_contract")
public class StandardFormContracts {
       
	@Id
	@GeneratedValue
	private int sfco_id;
	private String sfco_name;
	private int sfco_type_id;
	@Size(max = 3900)
	private String sfco_description;
	private Date sfco_created_at;
	private int sfco_added_by;
	private int sfco_entity_id;
	private int sfco_unit_id;
	private int sfco_function_id;
	@Size(max = 3900)
	private String sfco_abbreviation;
	private String sfco_contract_type;
	
	public String getSfco_abbreviation() {
		return sfco_abbreviation;
	}
	public void setSfco_abbreviation(String sfco_abbreviation) {
		this.sfco_abbreviation = sfco_abbreviation;
	}
	public String getSfco_contract_type() {
		return sfco_contract_type;
	}
	public void setSfco_contract_type(String sfco_contract_type) {
		this.sfco_contract_type = sfco_contract_type;
	}
	public int getSfco_entity_id() {
		return sfco_entity_id;
	}
	public void setSfco_entity_id(int sfco_entity_id) {
		this.sfco_entity_id = sfco_entity_id;
	}
	public int getSfco_unit_id() {
		return sfco_unit_id;
	}
	public void setSfco_unit_id(int sfco_unit_id) {
		this.sfco_unit_id = sfco_unit_id;
	}
	public int getSfco_function_id() {
		return sfco_function_id;
	}
	public void setSfco_function_id(int sfco_function_id) {
		this.sfco_function_id = sfco_function_id;
	}
	public int getSfco_id() {
		return sfco_id;
	}
	public void setSfco_id(int sfco_id) {
		this.sfco_id = sfco_id;
	}
	public String getSfco_name() {
		return sfco_name;
	}
	public void setSfco_name(String sfco_name) {
		this.sfco_name = sfco_name;
	}
	public int getSfco_type_id() {
		return sfco_type_id;
	}
	public void setSfco_type_id(int sfco_type_id) {
		this.sfco_type_id = sfco_type_id;
	}
	public String getSfco_description() {
		return sfco_description;
	}
	public void setSfco_description(String sfco_description) {
		this.sfco_description = sfco_description;
	}
	public Date getSfco_created_at() {
		return sfco_created_at;
	}
	public void setSfco_created_at(Date sfco_created_at) {
		this.sfco_created_at = sfco_created_at;
	}
	public int getSfco_added_by() {
		return sfco_added_by;
	}
	public void setSfco_added_by(int sfco_added_by) {
		this.sfco_added_by = sfco_added_by;
	}
	
	
}
