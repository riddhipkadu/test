package lcmt.domain;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.validation.constraints.Size;

@Entity(name="mst_ARC_manager")
public class ArcManager {
	
	@Id
	@GeneratedValue
	private int mgr_arc_id;
	private int mgr_arc_name;
	private String mgr_name;
	private String mgr_arc_contact_no;
	private String mgr_arc_email_id;
	@Size(max = 3900)
	private String mgr_arc_address;
	private Date mgr_arc_created_at;
	private Date mgr_arc_updated_at;
	private int mgr_arc_added_by;
	private String mgr_arc_enable_status;
	private String mgr_arc_approval_status;
	
	public int getMgr_arc_id() {
		return mgr_arc_id;
	}
	public void setMgr_arc_id(int mgr_arc_id) {
		this.mgr_arc_id = mgr_arc_id;
	}
	public int getMgr_arc_name() {
		return mgr_arc_name;
	}
	public void setMgr_arc_name(int mgr_arc_name) {
		this.mgr_arc_name = mgr_arc_name;
	}
	public String getMgr_name() {
		return mgr_name;
	}
	public void setMgr_name(String mgr_name) {
		this.mgr_name = mgr_name;
	}
	public String getMgr_arc_contact_no() {
		return mgr_arc_contact_no;
	}
	public void setMgr_arc_contact_no(String mgr_arc_contact_no) {
		this.mgr_arc_contact_no = mgr_arc_contact_no;
	}
	public String getMgr_arc_email_id() {
		return mgr_arc_email_id;
	}
	public void setMgr_arc_email_id(String mgr_arc_email_id) {
		this.mgr_arc_email_id = mgr_arc_email_id;
	}
	public String getMgr_arc_address() {
		return mgr_arc_address;
	}
	public void setMgr_arc_address(String mgr_arc_address) {
		this.mgr_arc_address = mgr_arc_address;
	}
	public Date getMgr_arc_created_at() {
		return mgr_arc_created_at;
	}
	public void setMgr_arc_created_at(Date mgr_arc_created_at) {
		this.mgr_arc_created_at = mgr_arc_created_at;
	}
	public Date getMgr_arc_updated_at() {
		return mgr_arc_updated_at;
	}
	public void setMgr_arc_updated_at(Date mgr_arc_updated_at) {
		this.mgr_arc_updated_at = mgr_arc_updated_at;
	}
	public int getMgr_arc_added_by() {
		return mgr_arc_added_by;
	}
	public void setMgr_arc_added_by(int mgr_arc_added_by) {
		this.mgr_arc_added_by = mgr_arc_added_by;
	}
	public String getMgr_arc_enable_status() {
		return mgr_arc_enable_status;
	}
	public void setMgr_arc_enable_status(String mgr_arc_enable_status) {
		this.mgr_arc_enable_status = mgr_arc_enable_status;
	}
	public String getMgr_arc_approval_status() {
		return mgr_arc_approval_status;
	}
	public void setMgr_arc_approval_status(String mgr_arc_approval_status) {
		this.mgr_arc_approval_status = mgr_arc_approval_status;
	}
	

}
